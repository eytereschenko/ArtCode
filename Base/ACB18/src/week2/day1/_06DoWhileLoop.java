public class _06DoWhileLoop {
    public static void main(String[] args) {
        int a = 10;

        while (a < 10){
            System.out.println(a);
            a++;
        }

        int b = 10;

        do{
            System.out.println(b);
            b++;
        }while (b < 10);
    }
}
