package week1;

public class _6Bool {

    public static void main(String[] args) {

        boolean yes = true;
        boolean no = false;
//      true && false = false
        boolean f = yes && no;
        System.out.println(f);

        boolean g = yes || no;
        System.out.println(g);


        boolean m = 30 > 40;

        System.out.println(m);


    }

}
