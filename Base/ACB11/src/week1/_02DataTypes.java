package week1;

public class _02DataTypes {
    public static void main(String[] args) {
        // create variable
        // <type> <name>;
        byte age;
//      write value
//      <name> = <value>;
        age = 20;


        // create variable
        // <type> <name> = <value>;
        byte age2 = 30;
        System.out.println(age);
        System.out.println(age2);
        int age3 = 12;
        System.out.println(age3);

        char letter = 'A';

        System.out.print(letter);

        char letter2 = 'B';
        char letter3 = 'C';
        System.out.println(letter2);


        System.out.print(letter3);


    }
}
