public class RelationOperations {
    public static void main(String[] args) {
        int a = 18;
        int b = 22;
        boolean bool = a > b;
        System.out.println(bool);
        bool = a < b;
        System.out.println(bool);
        bool = (a == b);
        System.out.println(bool);
        bool = (a != b);
        System.out.println(bool);
    }
}
