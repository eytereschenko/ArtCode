package week1.day1;


public class DataTypeCasting {
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);

        int code = c;
        System.out.println(code);

        int i = 25;
        byte b = (byte)i;
        System.out.println(b);

        int i2 = 200;
        byte b2 = (byte)i2;
        System.out.println(b2);

        char symbol = 98;
        int number = 'a';
        System.out.println(symbol);
        System.out.println(number);


    }
}
